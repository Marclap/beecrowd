#include <bits/stdc++.h>

using namespace std;

int main(){
    char pal[50] = {"AMO FAZER EXERCICIO NO URI"};
    printf("<%s>\n", pal);
    printf("<%30s>\n", pal);
    printf("<%.20s>\n", pal);
    printf("<%-20s>\n", pal);
    printf("<%-30s>\n", pal);
    printf("<%.30s>\n", pal);
    printf("<%30.20s>\n", pal);
    printf("<%-30.20s>\n", pal);
    return 0;
}